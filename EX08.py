# Atribuição e impressão de variáveis
idade = 39
print(idade)    # Imprime o valor de 'idade'

# Operações matemáticas e impressão do resultado
resultado = 4 * 10 - 2
print(resultado)    # Imprime o resultado de 4 * 10 - 2

# Atribuição e impressão de um valor booleano
x = True
print(x)    # Imprime o valor de 'x' (True)

# Atribuição e impressão de uma string
nome = 'Tainara Ferreira'
print(nome) # Imprime o nome

# Operação matemática com uma variável
x = 10
print(x * 5)    # Imprime 10 * 5

# Atribuição e impressão de nova variável
nova = x + 2
print(nova) # Imprime o valor de 'nova'
print(x)    # Imprime o valor de 'x' (não alterado)

# Atribuição e impressão de um valor float
exemplo = 5.6
print(exemplo)  # Imprime o valor de 'exemplo'

# Atribuição de novos valores para a variável
exemplo = 10.2
exemplo = 3.1
print(exemplo)  # Imprime o valor final de 'exemplo'

# Atribuição e impressão do peso
peso = 89.2
print(peso) # Imprime o peso inicial

# Operação e atualização do peso
peso = peso + 10
print(peso) # Imprime o peso atualizado

# Concatenação de strings e impressão do nome
nome = 'Gabriel'
nome = nome + ' Moraes'
print(nome) # Imprime o nome completo
