# Impressão de uma string com espaços extras no código (não afeta a saída)
print     (   "Experimento com espaços em branco"     )

# Impressão de uma string contendo múltiplos espaços dentro do texto
print("Experimento     com      espaços      em     branco")

# Impressão de múltiplos argumentos, separados por espaço padrão
print("Bom", "dia", "pessoal!")

# Impressão de múltiplos argumentos, separados por '---'
print("Bom", "dia", "pessoal!", sep="---")