# Atribuição de valor string e verificação do tipo
minhavar = '2024'
print(type(minhavar))  # Imprime <class 'str'>, pois 'minhavar' é uma string

# Atribuição de valor inteiro e verificação do tipo
minhavar = 2024
print(type(minhavar))  # Imprime <class 'int'>, pois 'minhavar' é um inteiro

# Atribuição de valor float e verificação do tipo
minhavar = 20.24
print(type(minhavar))  # Imprime <class 'float'>, pois 'minhavar' é um número decimal

# Atribuição de valor booleano e verificação do tipo
minhavar = True
print(type(minhavar))  # Imprime <class 'bool'>, pois 'minhavar' é um valor booleano
